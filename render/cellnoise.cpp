//---------------------------------------------------------------------
/** 1D point cell noise.
 */

CqVector3D CqCellNoise::PCellNoise1(TqFloat u)
{
	if(u<0.0)	u-=1;
	TqInt i=m_PermuteTable[((unsigned int)(u))&0x7ff];

	CqVector3D result;
	result.x(m_RandomTable[i]);
	i=m_PermuteTable[m_PermuteTable[i]+i];
	result.y(m_RandomTable[i]);
	i=m_PermuteTable[m_PermuteTable[i]+i];
	result.z(m_RandomTable[i]);

	return(result);
}


